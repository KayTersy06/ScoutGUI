    public void populatePlayersInfo()
    {
        try
        {
            BufferedWriter write;
        
            write = new BufferedWriter(new FileWriter(file, true));
       
            
            PlayersInfo playersInfo = playerInfo.get(playerInfo.size()-1);
            write.newLine();
            write.write(playersInfo.toString());
            
            
            
            write.close();
            
            message("Player info successfully saved!");
        }
        catch(IOException ioe)
        {
            message(ioe.getMessage());
        }
    }
    
