    private void readButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_readButtonActionPerformed
        File file = new File("C:\\Users\\kayte\\OneDrive\\Documentos\\OOP216D\\Assignment\\PlayersInfo.txt");
        
        try
        {
            BufferedReader reader = new BufferedReader(new FileReader(file));
            String msg, data = " ";
            
            while((msg=reader.readLine())!=null)
            {
                data += msg + "\n";
            }
            
            outputArea.setText(data);
            reader.close();
        }
        catch(IOException ioe)
        {
            message("Unable to open file!");
        }
    }//GEN-LAST:event_readButtonActionPerformed

    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        String name, surname, position, team;
        int age;
        
        name = namesField.getText();
        surname = surnameField.getText();
        position = positionField.getText();
        team = teamNameField.getText();
        age = Integer.parseInt(ageField.getText());
        
        PlayersInfo object = new PlayersInfo(name, surname, position, team, age);
        
        String str = object.checkAgeGroup(name, surname, position, team, age);
        
        outputArea.setText(str);
        
        if(name.trim().isEmpty() || surname.trim().isEmpty()
                || position.trim().isEmpty() || team.trim().isEmpty()) {
            String message = "Enter all fields required";
            outputArea.setText(message);
        }
        
        playerInfo.add(object);
        populatePlayersInfo();
    }//GEN-LAST:event_saveButtonActionPerformed

